SELECT `книги`.`название`,
    `авторы`.`имя`, `авторы`.`фамилия`
FROM `книги`
JOIN `авторство`
ON `книги`.`id` = `авторство`.`книга`
JOIN `авторы`
ON `авторство`.`автор` = `авторы`.`id`
JOIN `жанр_книги`
ON `книги`.`id` = `жанр_книги`.`книга`
JOIN `жанры`
ON `жанр_книги`.`жанр` = `жанры`.`id`
WHERE `жанры`.`название` = 'Фантастика'
;